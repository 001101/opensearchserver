package com.jaeksoft.searchlib.crawler.web.screenshot;

import java.net.URL;

public class ScreenshotMethodAll extends ScreenshotMethod {

	protected ScreenshotMethodAll(ScreenshotMethodEnum en) {
		super(en, "All");
	}

	@Override
	public boolean doScreenshot(URL url) {
		if (url == null)
			return false;
		return true;
	}
}
